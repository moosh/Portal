#include "Prefix.h"
#include "PortalGameModel.h"
#include "PortalGameView.h"
#include "PortalGameController.h"

/******************************************************************************

******************************************************************************/
PortalGameModel 		mModel;
PortalGameView			mView;
PortalGameController	mController;

/******************************************************************************

******************************************************************************/
int main(void)
{
	init();
	
	pinMode(kLeftButton, INPUT);
	pinMode(kRightButton, INPUT);
	pinMode(kUpButton, INPUT);
	pinMode(kDownButton, INPUT);
		
	randomSeed(analogRead(5));

	mModel.Init();
	mView.Init(&mModel);
	mController.Init(&mModel, &mView);

    for (;;)
	{
		mController.Tick();
	}
    
    return 0;
}

